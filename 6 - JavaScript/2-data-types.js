// Data Types - Primitive Data Types

// Numbers: Represents numeric value, 45 / -10 / 1.5
let age = 25;
console.log("The value of age:", age);
console.log("The type of age is:", typeof age);


// Strings: Represents textual values enclosed in '' or "", "abcd233!@#$"
let name = "John";
console.log("The value of name:", name);
console.log("The type of name is:", typeof name);

// Boolean: Either true or false
let isAdmin = true;
// isAdmin = false;
console.log("The value of isAdmin is:", isAdmin);
console.log("The data type of isAdmin is:", typeof isAdmin);


// Null: Represents intentional absence of value
let car = null;
console.log("The value of car is:", car);
console.log("The data type of car is:", typeof car);

// Undefined: Represent unintentional absence of value
let city;
console.log("The value of city is:", city);
console.log("The type of city is:", city);